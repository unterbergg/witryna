                        <p>Pertemuan digelar di Kantor Kemenhan, Jakarta Pusat, Senin (13/7/2020). Sandiaga mengatakan menciptakan ketahanan pangan memang cita-cita Prabowo sejak lama. Sandiaga turut menyampaikan selamat atas tugas baru Prabowo untuk menggarap lumbung pangan nasional.</p>
                        <p>"Kami juga memberikan selamat bahwa Bapak Prabowo Subianto mendapatkan amanah baru, satu challenge, tantangan baru, untuk mendirikan satu kekuatan dan ketahanan pangan, yang sudah menjadi memang cita-cita beliau sudah sejak lama," kata Sandiaga kepada wartawan, Selasa (14/7/2020).</p>
                        <p>Sandiaga mengklaim lumbung pangan nasional merupakan salah satu program yang dicetuskan Prabowo dan dirinya pada 2018. Gagasan itu juga bagian dari janji politik pada Pilpres 2019.</p>
                        <p>"Karena memang pemikiran lumbung pangan nasional itu adalah pemikiran yang memang berdua kami cetuskan di tahun 2018 dan 2019 dan tawarkan sebagai satu program untuk menjadi prioritas pembangunan bangsa, pembangunan Indonesia ke depan," tuturnya.</p>
